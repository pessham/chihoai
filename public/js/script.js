// 記事一覧を表示
function displayArticles() {
    const blogPostsContainer = document.querySelector('.blog-posts');
    if (!blogPostsContainer) return;
    blogPostsContainer.innerHTML = '';
    
    articles.forEach(article => {
        const postElement = createArticleCard(article);
        blogPostsContainer.appendChild(postElement);
    });
    
    
    // クリックイベントを設定
    setupArticleClickEvents();
}


// 記事カードを作成
function createArticleCard(article) {
    const postDiv = document.createElement('article');
    postDiv.className = 'blog-post';
    postDiv.setAttribute('data-article', article.id);
    
    postDiv.innerHTML = `
        <a href="articles/${article.id}.html" class="post-card-link">
            <div class="post-image" style="background-image: url('${article.thumbnail}');">
            </div>
            <div class="post-body">
                <h2 class="post-title">
                    ${article.title}
                </h2>
                <div class="post-content">
                    <p>${article.excerpt}</p>
                </div>
            </div>
        </a>
    `;
    
    return postDiv;
}

// スムーズスクロールの設定
function setupSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const target = document.querySelector(targetId);
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
}

// 記事クリックイベントの設定
function setupArticleClickEvents() {
    // 戻るリンクのクリック
    const backLink = document.querySelector('.back-link');
    if (backLink) {
        backLink.addEventListener('click', function(e) {
            e.preventDefault();
            showArticlesList();
        });
    }
}

// 記事詳細を表示
async function showArticleDetail(articleId) {
    const article = articles.find(a => a.id === articleId);
    if (!article) return;
    
    // 静的HTMLページにリダイレクト
    window.location.href = `articles/${articleId}.html`;
}

